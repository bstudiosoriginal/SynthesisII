class Input(object):

    def __init__(self, PHI1, PHI2, PHI3,) -> None:
        self.phi1 = PHI1
        self.phi2 = PHI2
        self.phi3 = PHI3


class IntermediateAngles():

    def __init__(self, gam1, gam2, gam3) -> None:
        self.gam1 = gam1
        self.gam2 = gam2
        self.gam3 = gam3


class PositionDisplacement(object):

    def __init__(self, R1, R2, R3) -> None:
        self.R1 = R1
        self.R2 = R2
        self.R3 = R3


class Output(object):

    def __init__(self, PSI1, PSI2, PSI3,) -> None:
        self.psi1 = PSI1
        self.psi2 = PSI2
        self.psi3 = PSI3
